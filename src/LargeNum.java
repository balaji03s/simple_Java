
public class LargeNum {

	public static void main(String[] args) {
		
        int a=100, b=110, c=120;
		
		if(a>b && a>c)
		{
		 System.out.println("A is Large Number: "+a);
		}
		else if(b>a && b>c)
		{
			System.out.println("B is Large Number:"+b);
		}
		else
		{
			System.out.println("C is Large Number: "+c);
		}
	}

}
